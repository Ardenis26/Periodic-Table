// Displays in to HTML File 
function a(d){
var elm = document.createElement("div");
 elm.setAttribute("class","elm");
 elm.setAttribute("id",`${d["id"]}`);
 elm.setAttribute("onmouseenter","hoverColor(event)")
 elm.setAttribute("onmouseleave","RMhoverColor(event)")
 elm.setAttribute("onclick","Details(event)")
 elm.style.backgroundColor=`${d["color"]}`;
 elm.style.left = `${d["position"][0]*120}px`;
 elm.style.top = `${d["position"][1]*120}px`;
 document.getElementById("Periodic-Table").appendChild(elm);

 var elmDetails = document.createElement("div");
 var elmindex = document.createElement("h1");
 var elmWeight = document.createElement("h1");
 var elmId = document.createElement("h1");
 var elmName = document.createElement('h1');

 elmDetails.setAttribute("class","emlDetails");
 elmindex.setAttribute("class","elmIndex");
 elmWeight.setAttribute("class","elmWeight");
 elmId.setAttribute("class","elmId");
 elmName.setAttribute("class","elmName");

 elmId.innerText=`${d["id"]}`;
 elmName.innerText=`${d["name"]}`;
 elmWeight.innerText=`${parseFloat(d["weight"]).toFixed(2)}`;
 elmindex.innerText=`${d["num"]}`;

 elm.appendChild(elmDetails)
 elmDetails.appendChild(elmindex)
 elmDetails.appendChild(elmWeight)
 elm.appendChild(elmId)
 elm.appendChild(elmName)
}
