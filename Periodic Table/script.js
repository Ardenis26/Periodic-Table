// Decrease and increase Opacity in to elements
function hoverColor(event){
 var elm = document.querySelector(`#${event.currentTarget.id}`);
 elm.style.opacity = "0.8"
}

function RMhoverColor(event){
 var elm = document.querySelector(`#${event.currentTarget.id}`);
 elm.style.opacity = "1"
}
